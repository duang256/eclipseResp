package cn.wit.advice;

import java.lang.reflect.Method;

import org.springframework.aop.AfterReturningAdvice;

public class MyAfterAdvice implements AfterReturningAdvice {

	@Override
	public void afterReturning(Object arg0, Method arg1, Object[] arg2, Object arg3) throws Throwable {
//		System.out.println("返回值"+arg0);
		System.out.println("切点方法对象："+arg1);
		System.out.println("方法名"+arg1.getName());
//		System.out.println("切点方法参数"+arg2[0]);
		System.out.println("方法所在对象"+arg3);
		System.out.println("我的后置通知");
	}

}
